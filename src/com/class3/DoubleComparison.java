package com.class3;

public class DoubleComparison {

	public static void main(String[] args) {
		double a = 15;
		double b = 10;

		if (a > b) {
			System.out.println("double value " + a + " is larger than " + b);
		} else {
			System.out.println("double value " + a + " is not larger than " + b);
		}

	}

}
