package com.class7;

public class postIncrment {

	public static void main(String[] args) {

		// post increment
		int a=90;
		int b=a++;
		System.out.println(a);//91
		System.out.println(b);//90
		
		
		// post decrement
		int c=100;
		int d=c--;
		System.out.println(c);//99
		System.out.println(d);//100
	}

}
