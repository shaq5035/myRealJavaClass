public class areaAndPerimeter {

	public static void main(String[] args) {
	
		int w=5;
		int h=8;
		int perimeter=(w+h)*2;
		
		System.out.println("The width of the rectangle is 5");		
		System.out.println("The height of the rectangle is 8");
		System.out.println("The area of this rectangle is " + (w * h));
		System.out.println("The perimeter of this rectangle is "+ perimeter);
	}

}
